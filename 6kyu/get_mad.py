def get_mad(arr: list) -> int:
    y = sorted(arr, reverse=True)
    dy = abs(int(y[0]) - int(y[-1]))
    i2 = y.pop()

    for i1 in y:
        if abs(i1 - i2) < dy:
            dy = abs(i1 - i2)
        i2 = i1
    return (dy)


